    public CargaEscenario1(int personaje, int nivel)
    {
        initComponents();
        this.personaje = personaje;
        switch (personaje)
        {
            case 1:
                Main m = new Main(personaje, nivel);
                gLCanvas2.addGLEventListener(m);
                break;
            case 2:
                SnowGirl s = new SnowGirl(personaje, nivel);
                gLCanvas2.addGLEventListener(s);
                break;
            case 3:
                MCowboy mc = new MCowboy(personaje, nivel);
                gLCanvas2.addGLEventListener(mc);
                break;
        }

        per = new Animator(gLCanvas2);
        per.start();

        setVisible(true);
        setSize(960, 550);
        //Generamos al centro de la pantalla con null
        setLocationRelativeTo(null);
        System.out.println("Cargando...");
        System.out.println("Personaje seleccionado: " + personaje);
        System.out.println("Nivel seleccionado: " + nivel);
    }

